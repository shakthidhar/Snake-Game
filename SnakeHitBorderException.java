
public class SnakeHitBorderException extends Exception{
	
	public SnakeHitBorderException() {
		super();
	}
	
	public SnakeHitBorderException(String message) {
		super(message);
	}
	
	public SnakeHitBorderException(String message, Throwable cause) {
		super(message, cause);
	}
	
	public SnakeHitBorderException(Throwable cause) {
		super(cause);
	}
	
}
